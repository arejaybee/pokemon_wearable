    /**
     * Gets an evolution based on a given pokemon's ID
     * @param id of the pokmeon to evolve
     */
    fun getEvolution(id: String): String {
        Log.i(TAG, "Evolving pokemon: $id")
        val evoID = map[id]?.evolvesTo.toString()
        if (evoID.contains("|")) {
            val evoList = evoID.split("|")
            Log.i(TAG, "Split Evolution pokemon: $evoID")
            val evoIndex = Random.nextInt(0, evoList.size)
            return evoList[evoIndex]
        }
        return evoID
    }

    /**
     * gets a random id from the firstEvolution list. This guarantees the first evolution of a given line of pokemon.
     */
    fun getRandomPokemonId(): Int {
        val num = Random.nextInt(0, firstEvolutions.size)
        val id = firstEvolutions[num]
        Log.i(TAG, "Split Evolution pokemon: $id")
        return id.toInt()
    }
